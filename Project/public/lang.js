function setLanguage(lang) {
  localStorage.setItem('language', lang);
  const data = translations[lang] || translations.en;
  
  // Elements with specific handling
  const searchBar = document.getElementById('searchBar');
  if (searchBar) searchBar.placeholder = data.searchPlaceholder || searchBar.placeholder;
  
  const receiveBtn = document.getElementById('receive');
  if (receiveBtn) receiveBtn.textContent = data.receive || receiveBtn.textContent;
  
  const sendBtn = document.getElementById('send');
  if (sendBtn) sendBtn.textContent = data.send || sendBtn.textContent;
  
  // Generic translation using data-i18n
  document.querySelectorAll('[data-i18n]').forEach((elem) => {
    const key = elem.dataset.i18n;
    if (data[key]) {
      if (elem.tagName === 'INPUT' || elem.tagName === 'TEXTAREA') {
        elem.placeholder = data[key];
      } else {
        // If the element contains an icon, preserve it
        const icon = elem.querySelector('i');
        if (icon) {
          const originalHTML = elem.innerHTML;
          const iconHTML = icon.outerHTML;
          const iconWasFirst = originalHTML.trim().startsWith('<i');
          
          if (iconWasFirst) {
            elem.innerHTML = `${iconHTML} ${data[key]}`;
          } else {
            elem.innerHTML = `${data[key]} ${iconHTML}`;
          }
        } else {
          elem.textContent = data[key];
        }
      }
    }
  });

  // Update button text if it exists
  const langToggle = document.getElementById('languageToggle');
  if (langToggle) {
    langToggle.textContent = lang === 'en' ? 'VI' : 'EN';
  }
}

function initLanguageSwitcher() {
  const savedLang = localStorage.getItem('language') || 'en';
  
  // Handle Toggle Button element
  const toggleBtn = document.getElementById('languageToggle');
  if (toggleBtn) {
    toggleBtn.addEventListener('click', () => {
      const currentLang = localStorage.getItem('language') || 'en';
      const newLang = currentLang === 'en' ? 'vi' : 'en';
      setLanguage(newLang);
    });
  }
  
  // Initial set
  setLanguage(savedLang);
}

document.addEventListener('DOMContentLoaded', () => {
  initLanguageSwitcher();
});
